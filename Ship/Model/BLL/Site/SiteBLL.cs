        public static int Subscribe(string Email)
        {
            try
            {
                Email = Email.ToEnNum();

                using (var x = new GolatehEntities())
                {
                    if (x.Subscribe.AsNoTracking().Any(v => v.Email.Trim().ToLower() == Email.Trim().ToLower()) == false)
                    {
                        x.Subscribe.Add(new Subscribe() { Email = Email.Trim().ToLower(), CreateDate = System.DateTime.Now });

                        x.SaveChanges();
                        return 1;
                    }
                    else
                    {
                        return 1;
                    }
                }
            }
            catch (Exception e)
            {
                return 0;
            }
        }
