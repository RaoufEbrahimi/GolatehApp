        public static int SendContactUs(string FullName, string Email, string Subject, string Message)
        {
            try
            {
                Email = Email.ToEnNum();

                using (var x = new GolatehEntities())
                {
                    x.ContactUs.Add(new ContactUs()
                    {
                        Email = Email.Trim().ToLower(),
                        CreateDate = System.DateTime.Now,
                        FullName = FullName,
                        Message = Message,
                        Title = "-"
                    });

                    x.SaveChanges();
                    return 1;
                }
            }
            catch (Exception e)
            {
                return 0;
            }
        }
